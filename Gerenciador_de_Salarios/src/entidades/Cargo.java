package entidades;

public class Cargo {
int IdCargo;
String NomeCargo;
float SalarioCargo;

public int getIdCargo() {
	return IdCargo;
}
public void setIdCargo(int idCargo) {
	IdCargo = idCargo;
}
public String getNomeCargo() {
	return NomeCargo;
}
public void setNomeCargo(String nomeCargo) {
	NomeCargo = nomeCargo;
}
public float getSalarioCargo() {
	return SalarioCargo;
}
public void setSalarioCargo(float salarioCargo) {
	SalarioCargo = salarioCargo;
}


}
